import { askGroq } from './groq';

const redFlags = ['шок', 'срочно', 'скрывают', '100%', 'без доказательств', 'все знают'];

export async function checkFact(text: string) {
  const answer = await askGroq(buildPrompt(text));
  if (answer)
    return `🤖 Разбор Groq:\n\n${answer}`;

  return heuristicCheck(text);
}

function heuristicCheck(text: string) {
  const lower = text.toLowerCase();
  const hits = redFlags.filter(flag => lower.includes(flag));

  if (!hits.length)
    return '⚠️ Groq не подключён или временно недоступен. По тестовым правилам явных признаков фейка не найдено.';

  return `⚠️ Groq не подключён или временно недоступен. Найдены признаки недостоверности: ${hits.join(', ')}. Проверь источник, дату, автора и подтверждения в независимых источниках.`;
}

function buildPrompt(text: string) {
  return `Разбери подозрительную новость или утверждение для школьника.

Сначала внимательно перепиши проверяемое утверждение своими словами, не меняя числа и смысл. Затем проверь именно его.

Формат ответа строго такой, без Markdown:
Вердикт: правда / ложь / манипуляция / недостаточно данных.
Почему: 2-4 коротких предложения. Если факт общеизвестный и проверяемый, можно прямо указать правильные данные.
Красные флаги: 2-4 пункта через тире.
Как проверить: 3 коротких шага.

Правила:
- не исправляй утверждение пользователя перед проверкой;
- если пользователь пишет «у собаки 5 лап», вердикт должен быть «ложь», потому что обычно у собаки 4 лапы;
- не обвиняй людей и организации без доказательств;
- если данных мало, напиши «недостаточно данных»;
- не пиши общие фразы вместо разбора конкретного текста;
- максимум 1600 символов.

Текст для проверки:
${text}`;
}
